import { mkdir, readdir, writeFile } from "node:fs/promises";
import path from "node:path";

const rootDir = path.resolve(import.meta.dirname, "..");
const outputPath = path.join(rootDir, "src", "data", "quiz-images.generated.ts");
const supportedExtensions = new Set([".jpg", ".jpeg", ".png", ".webp", ".avif"]);

const sources = [
  { answer: "ai", publicDir: "ai" },
  { answer: "human", publicDir: "human" },
];

function isSupportedImage(fileName) {
  return supportedExtensions.has(path.extname(fileName).toLowerCase());
}

function fileId(answer, fileName, index) {
  const encoded = Buffer.from(fileName).toString("base64url").slice(0, 16);
  return `${answer}-${index + 1}-${encoded}`;
}

async function listImagesForSource(source) {
  const directory = path.join(rootDir, "public", source.publicDir);

  await mkdir(directory, { recursive: true });

  const entries = await readdir(directory, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isFile() && isSupportedImage(entry.name))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b, "en"))
    .map((fileName, index) => ({
      id: fileId(source.answer, fileName, index),
      src: `/${source.publicDir}/${encodeURIComponent(fileName)}`,
      answer: source.answer,
      fileName,
    }));
}

const images = (await Promise.all(sources.map(listImagesForSource))).flat();

const file = `/* This file is generated by scripts/generate-image-manifest.mjs. */

export type QuizAnswer = "ai" | "human";

export type QuizImage = {
  id: string;
  src: string;
  answer: QuizAnswer;
  fileName: string;
};

export const quizImages: QuizImage[] = ${JSON.stringify(images, null, 2)} as QuizImage[];
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, file);

console.log(`Generated quiz manifest with ${images.length} image${images.length === 1 ? "" : "s"}.`);
